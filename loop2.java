public class loop2 {
    public static void main(String[] args) {
        // Q = print the numbers from 1 to 6.
    // Loops
    // initialisation, condition ,increment.
    for(int num = 1; num <= 6; num += 1){
        // Printing the number.
        System.out.println(num);
    }
    }
    
}
// Expected output is .
// 1
// 2
// 3
// 4
// 5
// 6