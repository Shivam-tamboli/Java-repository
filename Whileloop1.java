public class Whileloop1 {
    public static void main(String[] args) {
       
        // Initilization.
        int num = 1;
         // Using while loop.
        //  condition.
        while(num <= 9){
            // Printing the num.
            System.out.println(num);
            // Incrementation of num by one.
            num++;
        }
    }
    
}
// Expected output is.
// 1
// 2
// 3
// 4
// 5
// 6
// 7
// 8
// 9

